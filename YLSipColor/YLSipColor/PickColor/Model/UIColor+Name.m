static NSDictionary *colorNames;

@implementation UIColor (Name)
- (NSString *)yl_hexName
{
    const CGFloat *components = CGColorGetComponents(self.CGColor);
    
    CGFloat r = components[0];
    CGFloat g = components[1];
    CGFloat b = components[2];
    
    return [[NSString stringWithFormat:@"#%02lX%02lX%02lX",
             lroundf(r * 255),
             lroundf(g * 255),
             lroundf(b * 255)]uppercaseString];
}

- (NSString *)yl_commonName
{
    NSDictionary *dict = [[self class]yl_namesDict];
    NSString *hexName = [[self yl_hexName]lowercaseString];
    NSArray *keys = [dict allKeysForObject:hexName];
    if(keys.count > 0){
        return [keys firstObject];
    }
    return @"Undefined";
    
}

